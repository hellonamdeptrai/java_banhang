    private void timKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timKiemActionPerformed
        String tuKhoa = txt_timKiem.getText();
        if(tuKhoa == ""){
            getData(listSP);
        }
        else {
            ArrayList<SanPham> listSP_timKiem = new ArrayList<>();
            for (int i = 0; i < listSP.size(); i++) {
                if (listSP.get(i).getMaSanPham().contains(tuKhoa)){
                    listSP_timKiem.add(listSP.get(i));
                }
            }
            getData(listSP_timKiem);
        }
    }//GEN-LAST:event_timKiemActionPerformed

    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        String maSP = txt_maSanPham.getText();
        String tenSP = txt_tenSP.getText();
        String tenNSX = txt_tenNSX.getText();
        String donViTinh = txt_donViTinh.getText();
        String giaNhap = txt_giaNhap.getText();
        String giaBan = txt_giaBan.getText();
        String soLuong = txt_soLuong.getText();

        SanPham sp = new SanPham();
        if (maSP.equals("")) {
            JOptionPane.showMessageDialog(this, "Mã sản phẩm không được để trống !");
            return;
        }
        for (int i = 0; i < listSP.size(); i++) {
            if (maSP.equals(listSP.get(i).getMaSanPham())) {
                JOptionPane.showMessageDialog(this, "Mã bị trùng, hãy nhập lại");
                return;
            } else {
                sp.setMaSanPham(maSP);
            }
        }
        if (tenSP.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên sản phẩm không được để trống !");
            return;
        }
        sp.setTenSP(tenSP);
        if (tenNSX.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên nhà sản xuất không được để trống !");
            return;
        }
        sp.setTenNSX(tenNSX);
        if (donViTinh.equals("")) {
            JOptionPane.showMessageDialog(this, "Đơn vị tính không được để trống !");
            return;
        }
        sp.setDonViTinh(donViTinh);
        if (giaNhap.equals("")) {
            JOptionPane.showMessageDialog(this, "Giá nhập không được để trống !");
            return;
        }
        if (giaNhap.equals("0")) {
            JOptionPane.showMessageDialog(this, "Giá nhập không có giá trị !");
            return;
        }
        sp.setGiaNhap(giaNhap);
        if (giaBan.equals("")) {
            JOptionPane.showMessageDialog(this, "Giá bán không được để trống !");
            return;
        }
        if (giaBan.equals("0")) {
            JOptionPane.showMessageDialog(this, "Giá bán không có giá trị !");
            return;
        }
        sp.setGiaBan(giaBan);
        if (soLuong.equals("")) {
            JOptionPane.showMessageDialog(this, "Số lượng không được để trống !");
            return;
        }
        if (soLuong.equals("0")) {
            JOptionPane.showMessageDialog(this, "Số lượng không có giá trị !");
            return;
        }
        sp.setSoLuong(soLuong);

        listSP.add(sp);
        getData(listSP);
        saveFile();
        JOptionPane.showMessageDialog(this, "Thêm mới thành công !");
        reset();
    }//GEN-LAST:event_themActionPerformed

    private void xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa sản phẩm " + sp.getMaSanPham()+ " không ?", "Xóa sản phẩm", 2);
        if (confirm == JOptionPane.YES_OPTION) {
            if (listSP.remove(sp) == true) {
                saveFile();
                JOptionPane.showConfirmDialog(this, "Xóa thành công !");
                getData(listSP);
                reset();
            } else {
                JOptionPane.showConfirmDialog(this, "Xóa thất bại !");
            }
        }
    }//GEN-LAST:event_xoaActionPerformed

