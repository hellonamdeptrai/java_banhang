    private void bangsanphamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bangsanphamMouseClicked
        sua.setEnabled(true);
        xoa.setEnabled(true);
        // lấy ra bản ghi
        int row = bangsanpham.getSelectedRow();
        String maSP = bangsanpham.getValueAt(row, 0).toString();
        for (int i = 0; i < listSP.size(); i++) {
            if (maSP.equals(listSP.get(i).getMaSanPham())) {
                sp = listSP.get(i);
                index = i;
                break;
            }
        }
        txt_maSanPham.setEnabled(false);
        them.setEnabled(false);
        
        txt_maSanPham.setText(sp.getMaSanPham());
        txt_tenNSX.setText(sp.getTenNSX());
        txt_tenSP.setText(sp.getTenSP());
        txt_soLuong.setText(sp.getSoLuong());
        txt_donViTinh.setText(sp.getDonViTinh());
        txt_giaBan.setText(sp.getGiaBan());
        txt_giaNhap.setText(sp.getGiaNhap());
    }//GEN-LAST:event_bangsanphamMouseClicked


